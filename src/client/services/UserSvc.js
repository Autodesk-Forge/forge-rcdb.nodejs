
import ClientAPI from 'ClientAPI'
import BaseSvc from './BaseSvc'

export default class UserSvc extends BaseSvc {
  /// //////////////////////////////////////////////////////
  //
  //
  /// //////////////////////////////////////////////////////
  constructor (config) {
    super(config)

    this.api = new ClientAPI(config.apiUrl)
  }

  /// //////////////////////////////////////////////////////
  //
  //
  /// //////////////////////////////////////////////////////
  name () {
    return 'UserSvc'
  }

  /// //////////////////////////////////////////////////////
  //
  //
  /// //////////////////////////////////////////////////////
  getActiveModels (db) {
    const url = `/${db}/models`

    return this.api.ajax(url)
  }
}
