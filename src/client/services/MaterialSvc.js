
import ClientAPI from 'ClientAPI'
import BaseSvc from './BaseSvc'

export default class SocketSvc extends BaseSvc {

  /////////////////////////////////////////////////////////////////
  //
  //
  /////////////////////////////////////////////////////////////////
  constructor(opts) {

    super (opts)

    this._api = new ClientAPI(this._config.apiUrl)
  }

  /////////////////////////////////////////////////////////////////
  //
  //
  /////////////////////////////////////////////////////////////////
  name() {

    return 'MaterialSvc';
  }

  /////////////////////////////////////////////////////////////////
  //
  //
  /////////////////////////////////////////////////////////////////
  getMaterials() {

    var url = `${this._config.apiUrl}`

    return this._api.ajax(url)
  }
}
